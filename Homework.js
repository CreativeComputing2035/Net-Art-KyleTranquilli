function toggle_div_fun(id) {
      //!! toggle the appearance of the div//!!
    
    var divelement = document.getElementById(id);
    if(divelement.style.display == 'none')
        divelement.style.display = 'block';
    else
        divelement.style.display = 'none';
}








 
 


